// Null explicity set a variable with no value
// Undefined For variables that have not yet defined

let age;

console.log(age, age + 3, `the age is ${age}`); //undefined NaN the age is undefined

let a = null;

console.log(a, a + 3, `the age is ${a}`); //null 3 the age is null

let ab = 23;

console.log(ab, ab + 3, `the age is ${ab}`); //23 26 the age is 23
